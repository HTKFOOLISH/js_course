// if statement
let age = 20;

if (age >= 18) {
  console.log("đủ tuổi trưởng thành.");
}

// if-else statement
if (age >= 18) {
  console.log("đủ tuổi trưởng thành.");
} else {
  console.log("chưa đủ tuổi trưởng thành.");
}

// else if statement
if (age < 13) {
  console.log("trẻ em.");
} else if (age < 18) {
  console.log("thanh thiếu niên.");
} else if (age < 60) {
  console.log("người trưởng thành.");
} else {
  console.log("người cao tuổi.");
}
