// While Loop
console.log("=".repeat(50));

console.log("While Loop");
let a = 5;
console.log("In the loop:");
while (a < 5) {
  console.log("\t a = " + a);
  a++;
}
console.log("Out of the loop: a = " + a);

// Do while Loop
console.log("=".repeat(50));

console.log("Do...While Loop");
let b = 5;
console.log("In the loop:");
do {
  console.log("\tb = " + b);
  b++;
} while (b < 5);
console.log("Out of the loop: b = " + b);
